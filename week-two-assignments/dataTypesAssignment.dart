void main() {
  // Integer data type
  int age = 20;
  print("Age: $age");
  // double data type that covers decimals
  double height = 170.12;
  print("Height: $height");
  // String data type
  String name = "Joseph Akumu";
  print("Name: $name");
  // List of integers
  List<int> scores = [85, 89, 99, 90];
  print("Scores: $scores");
// Map to store list of planets as key value pairs.
  Map<String, String> planets = {
    "first": "Mercury",
    "second": "Venus",
    "third": "Earth",
    "fourth": "Mars",
    "fifth": "Jupiter"
  };

  print(planets["third"]);
}
